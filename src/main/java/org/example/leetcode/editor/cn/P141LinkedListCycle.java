    class Solution {

        //æ—¶é—´å¤æ‚è¯» O(N) ç©ºé—´å¤æ‚åº¦ O(1)
        public boolean hasCycle1(ListNode head) {
            Set<ListNode> nodeSet = new HashSet<>();
            while (head != null) {
                if (!nodeSet.add(head)) {
                    return true;
                }
                head = head.next;
            }
            return false;
        }

        //æ—¶é—´å¤æ‚åº¦ O(N) ç©ºé—´å¤æ‚åº¦ O(1)
        public boolean hasCycle(ListNode head){
            if (head == null || head.next == null){
                return false;
            }
            ListNode slow = head;
            ListNode fast = head.next;
            while (slow != fast){
                if (fast == null || fast.next == null){
                    return false;
                }
                slow = slow.next;
                fast = fast.next.next;
            }
            return true;
        }

    }

